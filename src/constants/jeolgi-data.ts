// 절기 이름 순서 (1월~12월)
const JEOLGI_NAMES: (keyof JeolgiEntry)[] = [
  '입춘', '경칩', '청명', '입하', '망종', '소서',
  '입추', '백로', '한로', '입동', '대설', '소한',
];

/**
 * 특정 연도의 절기 데이터를 가져옴
 * DB에 없는 연도는 가장 가까운 연도의 데이터로 보간
 */
export function getJeolgiForYear(year: number): JeolgiEntry {
  if (JEOLGI_DB[year]) return JEOLGI_DB[year];

  // 가장 가까운 연도 찾기
  const years = Object.keys(JEOLGI_DB).map(Number).sort((a, b) => a - b);
  let closest = years[0];
  let minDiff = Math.abs(year - closest);
  for (const y of years) {
    const diff = Math.abs(year - y);
    if (diff < minDiff) {
      minDiff = diff;
      closest = y;
    }
  }
  return JEOLGI_DB[closest];
}

/**
 * 정확한 절기 기반 월 계산
 * 양력 월/일을 받아서 절기 기준 월(1~12)을 반환
 * 1월 = 인월(입춘~경칩), 2월 = 묘월(경칩~청명), ...
 */
export function getAccurateJeolgiMonth(year: number, solarMonth: number, solarDay: number): number {
  const jeolgi = getJeolgiForYear(year);

  // 12절기를 역순으로 검사
  for (let i = JEOLGI_NAMES.length - 1; i >= 0; i--) {
    const name = JEOLGI_NAMES[i];
    const [jMonth, jDay] = jeolgi[name];
    const jeolgiMonthNum = i + 1; // 1~12

    if (solarMonth > jMonth || (solarMonth === jMonth && solarDay >= jDay)) {
      return jeolgiMonthNum;
    }
  }

  // 소한 이전이면 전년도 12월(축월)
  return 12;
}

/**
 * 정확한 입춘 기반 년도 보정
 */
export function getAccurateJeolgiYear(year: number, solarMonth: number, solarDay: number): number {
  const jeolgi = getJeolgiForYear(year);
  const [ipchunMonth, ipchunDay] = jeolgi.입춘;

  if (solarMonth < ipchunMonth || (solarMonth === ipchunMonth && solarDay < ipchunDay)) {
    return year - 1;
  }
  return year;
}
