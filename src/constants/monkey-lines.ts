export function getRandomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

// 오행 점수 기반으로 운세 좋은지 판단
export function isLuckyFortune(ohaengScores: Record<string, number>): boolean {
  const values = Object.values(ohaengScores);
  const max = Math.max(...values);
  const min = Math.min(...values);
  // 균형이 잘 잡혔으면 (차이가 작으면) 대박
  return (max - min) < 1.5;
}

// 재물운 좋은지 판단 (정재/편재가 있는지)
export function hasWealthStar(sipsinList: string[]): boolean {
  return sipsinList.some(s => s === '정재' || s === '편재');
}
